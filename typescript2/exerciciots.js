"use strict";
// Função de multiplicação
function multiplicarNumeros(a, b) {
    return a * b;
}
// Função de saudação
function saudacao(nome) {
    return "Olá " + nome;
}
// Exemplos de uso
const resultadoMultiplicacao = multiplicarNumeros(10, 1);
const mensagemSaudacao = saudacao("Dirlan");
console.log(resultadoMultiplicacao); // Saída: 15
console.log(mensagemSaudacao); // Saída: Olá Usuário
